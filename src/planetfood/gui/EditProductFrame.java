    private void jcbProductCategoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbProductCategoryActionPerformed
           try
        {
            String catName=jcbProductCategory.getSelectedItem().toString();
            String catId=categories.get(catName);
            productList=ProductDao.getProductsByCategory(catId);
            jcbProductId.removeAllItems();
            txtProductName.setText("");
            txtProductPrice.setText("");
            jcbIsActive.setSelected(false);
            for(String prodId:productList.keySet())
                jcbProductId.addItem(prodId);
              
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error in DB"+ex,"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace(); 
        }
    }//GEN-LAST:event_jcbProductCategoryActionPerformed

    private void jcbProductIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbProductIdActionPerformed
        if(jcbProductId.getSelectedItem()==null)
            return;
        String prodId=jcbProductId.getSelectedItem().toString();
        Product p=null;
        p=productList.get(prodId);
        txtProductName.setText(p.getProdName());
        txtProductPrice.setText(p.getProdPrice()+"");
        String isActive=p.getIsActive();
        if(isActive.equalsIgnoreCase("Y"))
            jcbIsActive.setSelected(true);
        else
            jcbIsActive.setSelected(false);
        
    }//GEN-LAST:event_jcbProductIdActionPerformed

    private void btnSaveChangesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveChangesActionPerformed
        try
        {
            Product p=new Product();
            
            if(validateInput()==false)
            { 
                JOptionPane.showMessageDialog(null,"Please fill all the details ","Empty Values",JOptionPane.ERROR_MESSAGE);
                return;
            }
            else
            {
                String prodId=jcbProductId.getSelectedItem().toString();
                String prodName=txtProductName.getText();
                String prodPrice=txtProductPrice.getText();
                double price=Double.parseDouble(prodPrice);
                String isActive=getIsActive();
                p.setIsActive(isActive);
                p.setProdName(prodName);
                p.setProdPrice(price);
                p.setProdId(prodId);
                if(ProductDao.editProduct(p))
                {
                    JOptionPane.showMessageDialog(null,"Record Updated Sucessfully","Success",JOptionPane.INFORMATION_MESSAGE);
                    txtProductName.setText("");
                    txtProductPrice.setText("");
                }
                else
                {
                    JOptionPane.showMessageDialog(null,"Sorry! Failure in updation","Error while editing",JOptionPane.ERROR_MESSAGE);
                }  
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error in sql!"+ex,"Input Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
            
        }
        catch(NumberFormatException ex)
        {
           JOptionPane.showMessageDialog(null,"please fill  the  numeric value in price feilds"+ex,"Input Error",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveChangesActionPerformed

