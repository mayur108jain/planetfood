    public void loadProductDetails()
    {
        try
        {
           categories=CategoryDao.getAllCategoryId();
           for(String catName:categories.keySet())
               jcbProductCategory.addItem(catName);
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error in DB"+ex,"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace(); 
        }
    }
    private boolean validateInput()
    {
        String prodName=txtProductName.getText();
        String prodPrice=txtProductPrice.getText();
        if(prodName.isEmpty()||prodPrice.isEmpty())
        {
            return false;
        }
        else
        {
            return true;
        }
        
    }
     private String getIsActive()
     {
        if(jcbIsActive.isSelected())
            return "Y";
        else 
            return "N"; 
    }

